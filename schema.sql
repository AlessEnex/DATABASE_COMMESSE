-- Auth helpers (Supabase)
-- Returns true if the current user's email is in whitelist_email and active.
create or replace function is_whitelisted_email()
returns boolean
language sql
stable
security definer
set search_path = public, auth
as $$
  select exists (
    select 1
    from public.whitelist_email w
    where w.email = (auth.jwt() ->> 'email')
      and w.attiva = true
  );
$$;

-- Returns true if the current user is an admin.
create or replace function is_admin()
returns boolean
language sql
stable
security definer
set search_path = public, auth
as $$
  select exists (
    select 1
    from public.utenti u
    where u.id = auth.uid()
      and u.ruolo = 'admin'
  );
$$;

-- Returns true if the current user is whitelisted and not a viewer.
create or replace function can_write()
returns boolean
language sql
stable
security definer
set search_path = public, auth
as $$
  select exists (
    select 1
    from public.utenti u
    where u.id = auth.uid()
      and u.ruolo <> 'viewer'
  ) and is_whitelisted_email();
$$;

