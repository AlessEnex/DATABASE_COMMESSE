create or replace function update_commessa_planner_dates(
  p_commessa_id uuid,
  p_data_ordine_telaio date,
  p_data_consegna_macchina date,
  p_data_arrivo_kit_cavi date,
  p_data_prelievo date
)
returns void
language plpgsql
security definer
set search_path = public, auth
as $$
declare
  v_role text;
begin
  select u.ruolo::text into v_role
  from public.utenti u
  where u.id = auth.uid();

  if v_role is null or v_role not in ('planner', 'admin', 'responsabile') then
    raise exception 'unauthorized';
  end if;

  update public.commesse
  set data_ordine_telaio = p_data_ordine_telaio,
      data_consegna_macchina = p_data_consegna_macchina,
      data_arrivo_kit_cavi = p_data_arrivo_kit_cavi,
      data_prelievo = p_data_prelievo
  where id = p_commessa_id;
end;
$$;

grant execute on function update_commessa_planner_dates(uuid, date, date, date, date) to authenticated;

create or replace function current_risorsa_id()
returns smallint
language sql
stable
security definer
set search_path = public, auth
as $$
  select r.id
  from public.risorse r
  where r.utente_id = auth.uid()
  limit 1;
$$;

create or replace function can_edit_attivita(target_risorsa_id smallint)
  returns boolean
language sql
stable
security definer
set search_path = public, auth
as $$
  select exists (
    select 1
    from public.utenti u
    where u.id = auth.uid()
      and (
        u.ruolo in ('admin', 'responsabile')
        or (u.ruolo = 'operatore' and current_risorsa_id() = target_risorsa_id)
      )
    ) and is_whitelisted_email();
  $$;

create or replace function can_edit_attivita_row(
  target_attivita_id uuid,
  target_risorsa_id smallint,
  target_assegnato_a uuid
)
returns boolean
language sql
stable
security definer
set search_path = public, auth
as $$
  select exists (
    select 1
    from public.utenti u
    where u.id = auth.uid()
      and (
        u.ruolo in ('admin', 'responsabile')
        or (
          u.ruolo = 'operatore'
          and (
            current_risorsa_id() = target_risorsa_id
            or target_assegnato_a = auth.uid()
          )
        )
      )
  ) and is_whitelisted_email();
$$;

-- On signup, create a profile if the user is whitelisted.
create or replace function handle_new_user()
returns trigger as $$
declare
  wl record;
begin
  select * into wl
  from public.whitelist_email w
  where w.email = new.email
    and w.attiva = true;

  if found then
    insert into public.utenti (id, email, ruolo, reparto_id, nome)
    values (
      new.id,
      new.email,
      wl.ruolo_predefinito,
      wl.reparto_id_predefinito,
      nullif(new.raw_user_meta_data ->> 'full_name', '')
    )
    on conflict (id) do nothing;
  end if;

  return new;
end;
$$ language plpgsql security definer set search_path = public, auth;


