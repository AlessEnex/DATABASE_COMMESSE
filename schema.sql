-- Database schema for commesse tracking (Supabase/Postgres)
-- Note: uses enums and basic constraints; adjust as needed.

-- Extensions
create extension if not exists pgcrypto;
create extension if not exists pg_trgm;

-- Enums
do $$
begin
  if not exists (select 1 from pg_type where typname = 'commessa_stato') then
    create type commessa_stato as enum ('nuova', 'in_corso', 'chiusa', 'sospesa');
  end if;
  if not exists (select 1 from pg_type where typname = 'reparto_stato') then
    create type reparto_stato as enum ('da_fare', 'in_corso', 'fatto');
  end if;
  if not exists (select 1 from pg_type where typname = 'ruolo_utente') then
    create type ruolo_utente as enum ('admin', 'responsabile', 'planner', 'operatore', 'viewer');
  end if;
  if not exists (select 1 from pg_type where typname = 'ruolo_assegnazione') then
    create type ruolo_assegnazione as enum ('owner', 'collaboratore');
  end if;
  if not exists (select 1 from pg_type where typname = 'attivita_stato') then
    create type attivita_stato as enum ('pianificata', 'in_corso', 'completata', 'annullata', 'non_necessaria');
  end if;
end $$;

-- Helper function for updated_at
create or replace function set_updated_at()
returns trigger as $$
begin
  new.updated_at = now();
  return new;
end;
$$ language plpgsql;

